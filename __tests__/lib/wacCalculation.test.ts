import { describe, it, expect } from 'vitest'
import { calcWac } from '@/lib/calculations'

describe('calcWac', () => {
  it('أول شراء (مخزون=0): التكلفة = قيمة الشراء ÷ كمية الشراء', () => {
    // اشترينا 10 وحدات بـ 100 ريال → تكلفة الوحدة = 10
    expect(calcWac(0, 0, 10, 100)).toBeCloseTo(10)
  })

  it('شراء إضافي: يُحسب المتوسط المرجّح', () => {
    // مخزون: 10 وحدات بـ 10 ر.س = 100
    // شراء جديد: 10 وحدات بـ 12 ر.س = 120
    // WAC = (100 + 120) / (10 + 10) = 11
    expect(calcWac(10, 10, 10, 120)).toBeCloseTo(11)
  })

  it('شراء مجاني (قيمة=0): تكلفة الوحدة تثبت', () => {
    // مخزون: 10 وحدات بـ 10 ر.س
    // شراء مجاني: 5 وحدات بسعر صفر → WAC يبقى 10
    expect(calcWac(10, 10, 5, 0)).toBe(10)
  })

  it('qty=0 + value=0: يُرجع null (لا عملية)', () => {
    expect(calcWac(10, 10, 0, 0)).toBeNull()
  })

  it('أول شراء + كمية=0: يُرجع null (لا يمكن القسمة)', () => {
    expect(calcWac(0, 0, 0, 100)).toBeNull()
  })

  it('تكلفة مرتفعة + مخزون كبير: ينحاز للمخزون القديم', () => {
    // 90 وحدة بـ 10 + 10 وحدة بـ 100 = 1900 / 100 = 19
    expect(calcWac(90, 10, 10, 1000)).toBeCloseTo(19)
  })
})
